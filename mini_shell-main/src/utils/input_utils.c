#include "../minishell.h"

int	str_isalpha(t_prompt *p)
{
	int	i;

	i = 0;
	while (p->cmd[i])
	{
		if (!ft_isalpha(p->cmd[i]) && p->cmd[i] != '32')
			return (0);
		i++;
	}
	return (1);
}
